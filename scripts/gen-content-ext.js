/* Generates js/content-ext.js — courses c13–c30, 12 drills each. Run: node scripts/gen-content-ext.js */
const fs = require("fs");
const path = require("path");
const { COURSES } = require("./courses-ext-data");

const CARD_RE = /^(?:[2-9]|T|J|Q|K|A)[cdhs]$/;

function esc(s) {
  return String(s).replace(/\\/g, "\\\\").replace(/"/g, '\\"').replace(/\n/g, "\\n");
}

function validateSpot(spot, cid, i) {
  const cards = [...spot.board, ...spot.hand];
  const seen = new Set();
  for (const c of cards) {
    if (!CARD_RE.test(c)) throw new Error(`${cid}-q${i + 1}: bad card ${c}`);
    if (seen.has(c)) throw new Error(`${cid}-q${i + 1}: duplicate ${c}`);
    seen.add(c);
  }
  const street = spot.street || "flop";
  const need = street === "flop" ? 3 : street === "turn" ? 4 : 5;
  if (spot.board.length !== need) throw new Error(`${cid}-q${i + 1}: board length ${spot.board.length} for ${street}`);
}

function emitLearn(course) {
  const id = course.id;
  let out = "";
  const keys = ["l1", "l2", "l3", "sum"];
  course.learn.forEach((row, i) => {
    const k = keys[i];
    out += `  reg("${id}.${k}.t", "${esc(row[0])}", "${esc(row[1])}");\n`;
    out += `  reg("${id}.${k}.b", "${esc(row[2])}", "${esc(row[3])}");\n`;
  });
  out += `  reg("${course.concept}", "${esc(course.conceptEn)}", "${esc(course.conceptZh)}");\n`;
  return out;
}

function emitLearnObj(id) {
  return (
    `  LEARN.${id} = [\n` +
    `    { titleKey: "${id}.l1.t", bodyKey: "${id}.l1.b" },\n` +
    `    { titleKey: "${id}.l2.t", bodyKey: "${id}.l2.b" },\n` +
    `    { titleKey: "${id}.l3.t", bodyKey: "${id}.l3.b" },\n` +
    `    { titleKey: "${id}.sum.t", bodyKey: "${id}.sum.b", summary: true },\n` +
    `  ];\n`
  );
}

function emitSpotSpec(spec) {
  const actions = spec.bet > 0 ? (spec.allIn ? "FACE_CALL" : "FACE") : "ACT";
  let lines = `      s: { en: "${esc(spec.s.en)}", zh: "${esc(spec.s.zh)}" },\n`;
  lines += `      lbl: { en: "${esc(spec.lbl.en)}", zh: "${esc(spec.lbl.zh)}" },\n`;
  lines += `      board: ${JSON.stringify(spec.board)}, pot: ${spec.pot}, bet: ${spec.bet}, pos: "${esc(spec.pos)}", hand: ${JSON.stringify(spec.hand)},\n`;
  if (spec.street && spec.street !== "flop") lines += `      street: "${spec.street}",\n`;
  if (spec.allIn) lines += `      allIn: true,\n`;
  lines += `      actions: ${actions}, correct: ${JSON.stringify(spec.correct)}, leak: "${spec.leak}",\n`;
  lines += `      fb: {\n`;
  for (const [act, text] of Object.entries(spec.fb)) {
    lines += `        ${act}: { en: "${esc(text.en)}", zh: "${esc(text.zh)}" },\n`;
  }
  lines += `      },\n`;
  return lines;
}

function emitQuestions(course) {
  course.spots.forEach((s, i) => validateSpot(s, course.id, i));
  if (course.spots.length !== 12) throw new Error(`${course.id}: expected 12 spots, got ${course.spots.length}`);
  let out = `  QUESTIONS.${course.id} = buildSpots("${course.id}", "${course.concept}", [\n`;
  for (const spec of course.spots) out += `    {\n${emitSpotSpec(spec)}    },\n`;
  out += `  ]);\n`;
  return out;
}

let body = `/* content-ext.js — courses c13–c30 (12 drills each). Generated by scripts/gen-content-ext.js */\n(function () {\n`;
for (const c of COURSES) body += emitLearn(c);
body += "\n";
for (const c of COURSES) body += emitLearnObj(c.id);
body += "\n";
for (const c of COURSES) body += emitQuestions(c);
body += `})();\n`;

const outPath = path.join(__dirname, "..", "js", "content-ext.js");
fs.writeFileSync(outPath, body, "utf8");
console.log("Wrote", outPath, "—", COURSES.length, "courses,", COURSES.length * 12, "spots");
